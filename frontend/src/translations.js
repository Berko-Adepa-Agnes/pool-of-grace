export const t = (key, lang) => {
  const keys = key.split('.');
  let value = translations[lang || 'en'];
  for (const k of keys) {
    if (value && value[k] !== undefined) {
      value = value[k];
    } else {
      return key;
    }
  }
  return value;
};
